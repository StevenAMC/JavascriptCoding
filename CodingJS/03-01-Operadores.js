
//*OPERADORES ARITMÉTICOS:
let a = 3,
	b = 2;
let z = a + b;
console.log("Resultado de la suma: " + z);
z = a - b;
console.log("Resultado de la resta: " + z);
z = a * b;
console.log("Res: " + z);
z = a / b;
console.log("Res: " + z);
z = a % b;
console.log("Res. op. módulo o residuo: " + z);
z = a ** b;
console.log("Res. op. exponente: " + z);

//*PREINCREMENTO:
z= ++a;
console.log(a);
console.log(z);

//*POSTINCREMENTO:
z= b++;
console.log(b);
console.log(z);

//*PREDECREMENTO:
z= --a;
console.log(a);
console.log(z);

//*POSTDRECREMENTO:
z= b--;
console.log(b);
console.log(z);

